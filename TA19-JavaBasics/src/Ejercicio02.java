
public class Ejercicio02 {

	public static void main(String[] args) {
		int N = 5;
	    double A = 4.56;
	    char C = 'a';
	    System.out.println("Variable N: " + N + " / Variable A: " + A + " / Variable C: " + C);
	    System.out.println("Suma de "+ N + " y " + A + " = " + (N + A));
	    System.out.println("Diferencia de "+ N + " y " + A + " = " + Math.abs(N - A));
	    System.out.println("Valor númerico de "+ C + " = " + (int)C);
	}
}